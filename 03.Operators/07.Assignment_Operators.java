package Java;

public class Assignment_Operators
{
    public static void main(String[] args)
    {
        int a = 1;  // 2진수로 0000 0000 0000 0000 0000 0000 0000 0001
        int b = 2;  // 2진수로 0000 0000 0000 0000 0000 0000 0000 0010

        a += 3; // a = a + 3과 같은 의미
                // a는 4
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0100
        b -= 4; // b = b - 4과 같은 의미
                // b는 -2
                // 2진수로 1111 1111 1111 1111 1111 1111 1111 1110
        a *= a; // a = a * a와 같은 의미
                // a는 16
                // 2진수로 0000 0000 0000 0000 0000 0000 0001 0000
        b /= b; // b = b / b와 같은 의미
                // b 는 1
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0001
        a %= b; // a = a % b와 같은 의미
                // a는 0
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0000

        a &= b; // a = a & b와 같은 의미
                // a는 0
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0000
        a |= b; // a = a | b와 같은 의미
                // a는 1
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0001
        a ^= 5; // a = a ^ 5와 같은 의미
                // a는 4
                // 2진수로 0000 0000 0000 0000 0000 0000 0000 0100

        a <<= 6;    // a = a << 6과 같은 의미
                    // a는 256
                    // 2진수로 0000 0000 0000 0000 0000 0001 0000 0000
        a >>= 7;    // a = a >> 7과 같은 의미
                    // a는 2
                    // 2진수로 0000 0000 0000 0000 0000 0000 0000 0010

        a = ~a;     // 1의 보수를 취해서 음수로 만들어준다.
                    // a는 -3
                    // 2진수로 1111 1111 1111 1111 1111 1111 1111 1101

        a >>>= 8;   // a = a >>> 8과 같은 의미
                    // a는 16777215
                    // 2진수로 0000 0000 1111 1111 1111 1111 1111 1111
    }
}